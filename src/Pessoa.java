public class Pessoa {
    private String nome;
    private int idade;
    private char sexo;
    public String getNome() {
        return nome;
    }
    public void setNome(String mudaNome) {
        nome = mudaNome;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int mudaIdade) {
        idade = mudaIdade;
    }
    public char getSexo() {
        return sexo;
    }
    public void setSexo(char mudaSexo) {
        sexo = mudaSexo;
    }
}